    private void btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarActionPerformed
        // TODO add your handling code here:
        Profesor profe = null;
        try {
            String nombre = txt_nomb.getText();
            String apellido = txt_apell.getText();
            String cedula = txt_id.getText();
            String ecivil = txt_ecivil.getText();
            Date fechaIncorp = new SimpleDateFormat("dd/MM/yyyy").parse(txt_incor.getText());
            Departamento depto = new Departamento(txt_depto.getText());
            Despacho despacho = new Despacho(txt_desp.getText());
            if (!"".equals(nombre) && !"".equals(apellido) && !"".equals(cedula) && !"".equals(ecivil)) {
                //Profesor(Departamento depto, Despacho despacho, Date fechaIncor, String id, String nombre, String apellido, String eCivil, despacho, fechaIncor, id, nombre, apellido, eCivil);
                profe = new Profesor(depto, despacho, fechaIncorp, cedula, nombre, apellido, ecivil);
            }
            if (addDbProfesores(profe)) {
                JOptionPane.showMessageDialog(rootPane, "Profesor guardado");
                limpiar();
            } else {
                JOptionPane.showMessageDialog(rootPane, "No se puede guardar");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error, verifique que la fecha u otros campos estén correctos" + e);
        }
    }//GEN-LAST:event_btn_agregarActionPerformed

    private void btn_listarProfeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_listarProfeActionPerformed
        // TODO add your handling code here:
        ArrayList<Profesor> datosProfesor = listarProfesores();
        DefaultTableModel model;
        //Departamento depto, Despacho despacho, Date fechaIncor, String id, String nombre, String apellido, String eCivil
        String[] columnas = {"id", "nombre", "apellido", "eCivil", "dpto", "desp", "incorp"};
        model = new DefaultTableModel(null, columnas);
        String[] filas = new String[7];
        for (Profesor profe : datosProfesor) {
            filas[0] = profe.getId();
            filas[1] = profe.getNombre();
            filas[2] = profe.getApellido();
            filas[3] = profe.geteCivil();
            filas[4] = profe.devolverDpto();
            filas[5] = profe.devolverDesp();
            filas[6] = String.valueOf(profe.getFechaIncor());
            model.addRow(filas);
        }
        tbl_tabla.setModel(model);
    }//GEN-LAST:event_btn_listarProfeActionPerformed

