    public boolean addDbProfesores(Profesor newProfesor) {
        if (newProfesor == null) {
            return false;
        }
        if (!this.dbProfesores.contains(newProfesor)) {
            this.dbProfesores.add(newProfesor);
            return true;
        } else {
            return false;
        }
    }
    public boolean addDbEmServicio(Servicio newServicio) {
        if (newServicio == null) {
            return false;
        }
        if (!this.dbEmpServicio.contains(newServicio)) {
            this.dbEmpServicio.add(newServicio);
            return true;
        } else {
            return false;
        }
    }



    public ArrayList<Profesor> listarProfesores() {

        return dbProfesores;
    }
    public ArrayList<Servicio> listarServicio() {

        return dbEmpServicio;
    }
